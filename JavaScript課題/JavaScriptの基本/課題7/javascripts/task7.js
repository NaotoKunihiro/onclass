var num1 = 10;
var num2 = 20;

if (num1 === 10 && num2 === 20) {
    console.log(true);
} else {
    console.log(false);
}

if (num1 === 10 || num2 === 20) {
    console.log(true);
} else {
    console.log(false);
}

if (num1 !== 10 || num2 !== 20) {
    console.log(true);
} else {
    console.log(false);
}